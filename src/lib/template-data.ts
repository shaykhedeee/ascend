export function searchTemplates(query: string): { habits: HabitTemplate[]; goals: GoalTemplate[] } {
  const lowerQuery = query.toLowerCase();
  return {
    habits: HABIT_TEMPLATES.filter(h => 
      h.name.toLowerCase().includes(lowerQuery) ||
      h.description.toLowerCase().includes(lowerQuery) ||
      h.tags?.some(t => t.toLowerCase().includes(lowerQuery))
    ),
    goals: GOAL_TEMPLATES.filter(g =>
      g.title.toLowerCase().includes(lowerQuery) ||
      g.description.toLowerCase().includes(lowerQuery) ||
      g.tags?.some(t => t.toLowerCase().includes(lowerQuery))
    ),
  };
}
